<?php

// Operações de Incremento e Decremento.

// Servem para somar ou subtrair em (+1 ou -1)
    //qualquer valor do tipo numerico ou string. 

$valor = 20;
//pré-incremento
echo ++$valor;
echo "<hr>";
//pós incremento
echo $valor++;
echo "<br>";
echo $valor;
echo "<hr>";
//pré decremento
echo --$valor;
echo "<br>";
//pós Decremento
echo $valor--;
echo "<br>";
echo $valor;